package com.claireyurev;

public class Main {

    public static void main(String[] args) {

        // Augmented or COMPOUND assignment operators:
        // +=, -=, *=, /=, %=


        int x = 1;
        x = x + 2;
        System.out.println(x);

        // EXACTLY IDENTICAL TO THE ABOVE IS THIS:

        int y = 1;
        y += 2;
        System.out.println(y);

    }
}
