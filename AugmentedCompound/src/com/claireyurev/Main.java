package com.claireyurev;
/*
* Augmented and compound assignment operators
*
*  Operator	Description
*     +=	Addition
*     -=	Subtraction
*     *=	Multiplication
*     /=	Division
*     %=	Modulus
*     <<=	Left bit shift
*     >>=	Right bit shift
*     &=	Bitwise AND
*     ^=	Bitwise exclusive OR
*     |=	Bitwise inclusive OR
*
* */
public class Main {

    public static void main(String[] args) {

        int x = 11;

        x %= 3;

        System.out.println(x);
    }
}
